package viikko1.perusteet.th;

public class IntTietotyyppi {

    public static void main(String[] args) {
        // int tietotyyppi tarkoittaa kokonaislukua
        System.out.println(2 * 6);

        // int-arvoja voidaan laittaa talteen int-muuttujiin:
        int leveys = 2;
        int korkeus = 6;

        int pintaAla = leveys * korkeus;

        System.out.println(pintaAla);
    }
}
